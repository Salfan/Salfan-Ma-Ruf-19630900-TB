    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
 if (txtnama.getText().trim().equals("")){
 JOptionPane.showMessageDialog(null, "Nama_Pc Belum diisi");
 txtnama.requestFocus();
}else if (txtbayar.getText().trim().equals("")){
 JOptionPane.showMessageDialog(null, "Bayar Belum diisi");
 txtbayar.requestFocus();
}else if (txttgl.getText().trim().equals("")){
 JOptionPane.showMessageDialog(null, "Waktu Belum diisi");
 txttgl.requestFocus();
}else if (txtwaktu.getText().trim().equals("")){
 JOptionPane.showMessageDialog(null, "Waktu Belum diisi");
 txtwaktu.requestFocus();
}else if (new koneksi().DuplicateKey("Nama_pc","Nama_pc",txtnama.getText())){
 JOptionPane.showMessageDialog(null, "Nama_pc Sudah Terdaftar");
 txtnama.requestFocus();
}else{
String[] field ={"Nama_pc","Bayar","Waktu","Tanggal"};
String[] isi
={txtnama.getText(),txtbayar.getText(),txtwaktu.getText(),txttgl.getText()};
 new koneksi().SimpanDinamis("transaksi", field, isi);
 new koneksi().settingJudulTabel(Tabletransaksi, Judul);
 new koneksi().tampilTabel(Tabletransaksi, SQL, Judul);
 new koneksi().settingLebarKolom(Tabletransaksi, Lebar);
 
 }
    }//GEN-LAST:event_SimpanActionPerformed

