    private void TabletransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabletransaksiMouseClicked
        // TODO add your handling code here:
int row = Tabletransaksi.getSelectedRow();
        txtnama.setText(Tabletransaksi.getModel().getValueAt(row,0).toString());
        txtbayar.setText(Tabletransaksi.getModel().getValueAt(row,1).toString());
        txtwaktu.setText(Tabletransaksi.getModel().getValueAt(row,2).toString());
        txttgl.setText(Tabletransaksi.getModel().getValueAt(row,3).toString());
    }//GEN-LAST:event_TabletransaksiMouseClicked

    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        // TODO add your handling code here:
 new koneksi().HapusDinamis("transaksi", "Nama_pc", txtnama.getText());
        JOptionPane.showMessageDialog(null, "Berhasil Dihapus");
        new koneksi().settingJudulTabel(Tabletransaksi, Judul);
        new koneksi().tampilTabel(Tabletransaksi, SQL, Judul);
        new koneksi().settingLebarKolom(Tabletransaksi, Lebar);
        txtnama.setText("");
        txtbayar.setText("");
        txtwaktu.setText("");
        txttgl.setText("");
    }//GEN-LAST:event_HapusActionPerformed

    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
try {
            String[] field = {"Nama_Pc","Bayar","Waktu","Tanggal"};
            String[] isi = {txtnama.getText(),txtbayar.getText(),txtwaktu.getText(),txttgl.getText()};
            new koneksi().UbahDinamis("transaksi", "Nama_Pc", txtnama.getText(), field, isi);
            JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
            new koneksi().settingJudulTabel(Tabletransaksi, Judul);
            new koneksi().tampilTabel(Tabletransaksi, SQL, Judul);
            new koneksi().settingLebarKolom(Tabletransaksi, Lebar);
        } catch (Exception e){
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_EditActionPerformed

